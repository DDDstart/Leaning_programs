# Операторы приращения
a = 1
a += 1 # a=a+1
print(a)
a = 1
a -= 1 # a=a-1
print(a)
a = 2
a *= 3 # a=a*3
print(a)
a = 8
a /= 4 # a=a/4 (Вещественное число (float))
print(a)
a = 8
a //= 4 # a=a/4 (Целое число (int))
print(a)
a = 9
a %= 4 # a=a%4 (Остаток от деления)
print(a)
a = 2
a **= 4  # a=a**4 (Возведение в степень)
print(a)
